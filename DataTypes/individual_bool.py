# Boolean :Useful in conditional expressions
# Defined by true and false keywords
a = True
print(a)   # True
b = False
print(b)   # False

# Boolean Default values
c = False
d = bool()
print(c)   # False
print(d)  # False

# Operations on Boolean
e = bool(1)
print(e)   # True
f = bool(0)
print(f)    # False
g = bool(-1)
print(g)  # True
h = bool(1.0)
print(h)  # True
i = bool(2+3j)
print(i) # True
